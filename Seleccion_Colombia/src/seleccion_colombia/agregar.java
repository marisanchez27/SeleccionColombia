    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
         // TODO add your handling code here:
         PreparedStatement pps = con.prepareStatement("INSERT INTO equipo(id, nombres, apellido, posicion, edad, equipo, camisa) VALUES (?,?,?,?,?,?,?)");
         pps.setString(1, id.getText());
         pps.setString(2, nom.getText());
         pps.setString(3, ape.getText());
         pps.setString(4, posi.getText());
         pps.setString(5, edad.getText());
         pps.setString(6, equipo.getText());
         pps.setString(7, camisa.getText());
         pps.executeUpdate();
         JOptionPane.showMessageDialog(null, "Cita guardada exitosamente");
         
     } catch (SQLException ex) {
         Logger.getLogger(agregar.class.getName()).log(Level.SEVERE, null, ex);
     }
    }//GEN-LAST:event_jButton1ActionPerformed

